/**
 * 用户类型选项和值
 */
public class OptionValue {
    //选项名
    private String name;
    //选项值
    private String value;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
